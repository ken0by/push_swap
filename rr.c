#include "push_swap.h"

void	ra(char *a)
{
	int		i;
	int		j;
	char	*s;

	i = 1;
	j = 0;
	s = NULL;
	while (a[i])
	{
		s[j] = a[i];
		j++;
		i++;
	}
	s[j] = a[0];
}
